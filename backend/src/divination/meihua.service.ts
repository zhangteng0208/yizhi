export interface MeihuaResult {
  /** 姓名 */
  name: string;
  /** 每个字的康熙笔画 */
  strokes: { char: string; stroke: number }[];
  /** 姓笔画总数 */
  xingTotal: number;
  /** 名笔画总数 */
  mingTotal: number;
  /** 总笔画 */
  totalStrokes: number;
  /** 上卦 */
  shangGua: { index: number; name: string; symbol: string; wuxing: string; fangwei: string; xiangtian: string };
  /** 下卦 */
  xiaGua: { index: number; name: string; symbol: string; wuxing: string; fangwei: string; xiangtian: string };
  /** 动爻（1-6） */
  dongYao: number;
  /** 本卦 */
  benGua: { name: string; guaci: string; tuan: string; xiang: string; yao: string[] };
  /** 变卦 */
  bianGua: { name: string; guaci: string; tuan: string; xiang: string; yao: string[] };
  /** 互卦 */
  huGua: { name: string; guaci: string; tuan: string; xiang: string; yao: string[] };
}

@Injectable()
export class MeihuaService {
  private readonly logger = new Logger(MeihuaService.name);

  /**
   * 姓名起卦
   * @param name 姓名（至少2个字）
   * @param xingLength 姓的字数（默认1，复姓传2）
   */
  calculate(name: string, xingLength: number = 1): MeihuaResult {
    const chars = [...name];
    if (chars.length < 2) throw new Error('姓名至少需要2个字');

    // 查每个字的康熙笔画
    const strokes = chars.map(c => ({
      char: c,
      stroke: KANGXI_STROKES[c] ?? this.fallbackStroke(c),
    }));

    // 姓和名的笔画
    const xingChars = strokes.slice(0, xingLength);
    const mingChars = strokes.slice(xingLength);
    const xingTotal = xingChars.reduce((s, c) => s + c.stroke, 0);
    const mingTotal = mingChars.reduce((s, c) => s + c.stroke, 0);
    const totalStrokes = xingTotal + mingTotal;

    // 上卦：姓笔画 % 8（0当8，即坤）
    const shangIdx = xingTotal % 8 || 8;
    // 下卦：名笔画 % 8
    const xiaIdx = mingTotal % 8 || 8;
    // 动爻：总笔画 % 6（0当6）
    const dongYao = totalStrokes % 6 || 6;

    const shangName = BA_GUA[shangIdx];
    const xiaName = BA_GUA[xiaIdx];

    // 本卦
    const benKey = `${shangIdx}-${xiaIdx}`;
    const benGua = GUA64[benKey] || { name: '未知卦', guaci: '', tuan: '', xiang: '', yao: [] };

    // 变卦：动爻所在的卦（上卦1-3爻，下卦4-6爻）变阴阳
    const bianGua = this.calcBianGua(shangIdx, xiaIdx, dongYao);

    // 互卦：本卦2-4爻为下卦，3-5爻为上卦
    const huGua = this.calcHuGua(shangIdx, xiaIdx);

    return {
      name,
      strokes,
      xingTotal,
      mingTotal,
      totalStrokes,
      shangGua: {
        index: shangIdx,
        name: shangName,
        symbol: BA_GUA_SYMBOL[shangIdx],
        ...GUA_ATTR[shangName],
      },
      xiaGua: {
        index: xiaIdx,
        name: xiaName,
        symbol: BA_GUA_SYMBOL[xiaIdx],
        ...GUA_ATTR[xiaName],
      },
      dongYao,
      benGua,
      bianGua,
      huGua,
    };
  }

  /** 回退笔画：用 Unicode 编码估算 */
  private fallbackStroke(char: string): number {
    const code = char.charCodeAt(0);
    if (code >= 0x4e00 && code <= 0x9fff) {
      return Math.round((code - 0x4e00) / 2000) + 8;
    }
    return 10;
  }

  /** 八卦的三爻（从下到上）：1为阳，0为阴 */
  private guaYao(idx: number): [number, number, number] {
    const map: Record<number, [number, number, number]> = {
      1: [1, 1, 1], // 乾
      2: [0, 1, 1], // 兑
      3: [1, 0, 1], // 离
      4: [0, 0, 1], // 震
      5: [1, 1, 0], // 巽
      6: [0, 1, 0], // 坎
      7: [1, 0, 0], // 艮
      8: [0, 0, 0], // 坤
    };
    return map[idx] || [0, 0, 0];
  }

  /** 三爻转八卦索引 */
  private yaoToGua(yao: [number, number, number]): number {
    const map: Record<string, number> = {
      '1,1,1': 1, '0,1,1': 2, '1,0,1': 3, '0,0,1': 4,
      '1,1,0': 5, '0,1,0': 6, '1,0,0': 7, '0,0,0': 8,
    };
    return map[yao.join(',')] || 8;
  }

  /** 计算变卦 */
  private calcBianGua(shangIdx: number, xiaIdx: number, dongYao: number) {
    const xiaYao = [...this.guaYao(xiaIdx)] as [number, number, number];
    const shangYao = [...this.guaYao(shangIdx)] as [number, number, number];
    // 六爻：下卦1-3，上卦4-6
    if (dongYao <= 3) {
      xiaYao[dongYao - 1] = xiaYao[dongYao - 1] === 1 ? 0 : 1;
    } else {
      shangYao[dongYao - 4] = shangYao[dongYao - 4] === 1 ? 0 : 1;
    }
    const newShang = this.yaoToGua(shangYao);
    const newXia = this.yaoToGua(xiaYao);
    const key = `${newShang}-${newXia}`;
    return GUA64[key] || { name: '未知卦', guaci: '', tuan: '', xiang: '', yao: [] };
  }

  /** 计算互卦 */
  private calcHuGua(shangIdx: number, xiaIdx: number) {
    const xiaYao = this.guaYao(xiaIdx);
    const shangYao = this.guaYao(shangIdx);
    // 六爻排列（从下到上）：xia[0] xia[1] xia[2] shang[0] shang[1] shang[2]
    const allYao = [...xiaYao, ...shangYao];
    // 互卦下卦：2-4爻（index 1,2,3）
    const huXia: [number, number, number] = [allYao[1], allYao[2], allYao[3]];
    // 互卦上卦：3-5爻（index 2,3,4）
    const huShang: [number, number, number] = [allYao[2], allYao[3], allYao[4]];
    const huShangIdx = this.yaoToGua(huShang);
    const huXiaIdx = this.yaoToGua(huXia);
    const key = `${huShangIdx}-${huXiaIdx}`;
    return GUA64[key] || { name: '未知卦', guaci: '', tuan: '', xiang: '', yao: [] };
  }
}
